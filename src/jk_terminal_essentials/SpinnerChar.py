

import typing





#
# This is a stateful object that provides a spinning char only.
#
class SpinnerChar(object):

	################################################################################################################################
	## Constants
	################################################################################################################################

	__CHARACTERS = "|/-\\"

	################################################################################################################################
	## Constructor
	################################################################################################################################

	#
	# Constructor method.
	#
	def __init__(self):
		self.__chars = SpinnerChar.__CHARACTERS
		self.__pos = 0
	#

	################################################################################################################################
	## Public Properties
	################################################################################################################################

	@property
	def currentChar(self) -> str:
		return self.__chars[self.__pos]
	#

	################################################################################################################################
	## Helper Methods
	################################################################################################################################

	################################################################################################################################
	## Public Methods
	################################################################################################################################

	def turn(self) -> str:
		self.__pos = (self.__pos + 1) % len(self.__chars)
		return self.__chars[self.__pos]
	#

#





